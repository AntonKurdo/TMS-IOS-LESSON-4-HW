class UniversalCountClass {
   static func universalCount(sumFirstInt: Int, sumSecondInt: Int) -> Int {
        return sumFirstInt + sumSecondInt
    }
    
    static func universalCount(subtractFirstInt: Int, subtractSecontInt: Int) -> Int{
        return subtractFirstInt - subtractSecontInt
    }
    
    static func universalCount(multFirstInt: Int, multSecontInt: Int) -> Int{
        return multFirstInt * multSecontInt
    }
    
    static func universalCount(divisionFirstInt: Int, divisionSecontInt: Int) -> Int{
        return divisionFirstInt / divisionSecontInt
    }
}
